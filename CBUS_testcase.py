# Set seed for reproducibility
random.seed(42)


def generate_symmetric_distance_matrix(n):
    matrix = [[0 for _ in range(2 * n + 1)] for _ in range(2 * n + 1)]
    for i in range(2 * n + 1):
        for j in range(i + 1, 2 * n + 1):
            distance = random.randint(1, 100)
            matrix[i][j] = distance
            matrix[j][i] = distance
    return matrix


